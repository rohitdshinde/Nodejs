function add(number1, number2){
    return number1+number2
}

function sub(number1, number2){
    return number1 - number1;
}

function mul(number1, number2){
    return number1*number2;
}

function div(number1, number2){
    return number1/number2;
}

module.exports ={
    add, sub, mul, div
}